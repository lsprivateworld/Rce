package com.xd.rce.domain.perform;

public class AnalyseModelProcess {
    private int id;
    private String modelname;
    private String modelprocess;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getModelname() {
        return modelname;
    }

    public void setModelname(String modelname) {
        this.modelname = modelname;
    }

    public String getModelprocess() {
        return modelprocess;
    }

    public void setModelprocess(String modelprocess) {
        this.modelprocess = modelprocess;
    }
}
